=begin
input: array
ouput: same array
rules:
- output will return new array
- output will have input elements in reverse order
- don't use reverse methods

examples:
reverse([1,2,3,4]) == [4,3,2,1]          # => true
reverse(%w(a b e d c)) == %w(c d e b a)  # => true
reverse(['abc']) == ['abc']              # => true
reverse([]) == []                        # => true

list = [1, 3, 2]                      # => [1, 3, 2]
new_list = reverse(list)              # => [2, 3, 1]
list.object_id != new_list.object_id  # => true
list == [1, 3, 2]                     # => true
new_list == [2, 3, 1]                 # => true

algorithm:
- set reversed to empty array
- set counter to size of input array - 1
- while counter >= 0:
  - push array at counter to reversed
  - decrement counter by 1
- return reversed
=end

def reverse(array)
  reversed = []
  i = array.size - 1
  while i >= 0
    reversed << array[i]
    i -= 1
  end
  reversed
end

